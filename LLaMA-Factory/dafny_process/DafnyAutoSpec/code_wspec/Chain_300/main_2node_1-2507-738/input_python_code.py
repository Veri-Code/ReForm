def smallestValue_2507(n: int) -> int:
    # input: 2 <= n <= 100000
    # output: 0 <= <output> <= 1000000000
    while 1:
        (t, s, i) = (n, 0, 2)
        while i <= n // i:
            while n % i == 0:
                n //= i
                s += i
            i += 1
        if n > 1:
            s += n
        if s == t:
            return t
        n = s


def monotoneIncreasingDigits_738(n: int) -> int:
    # input: 0 <= n <= 1000000000
    # output: unconstrained
    s = list(str(n))
    i = 1
    while i < len(s) and s[i - 1] <= s[i]:
        i += 1
    if i < len(s):
        while i and s[i - 1] > s[i]:
            s[i - 1] = str(int(s[i - 1]) - 1)
            i -= 1
        i += 1
        while i < len(s):
            s[i] = "9"
            i += 1
    return int("".join(s))


def main_2node_1(o: object) -> object:
    """2 nodes, single chain"""
    o1: object = smallestValue_2507(o)
    o2: object = monotoneIncreasingDigits_738(o1)
    return o2
